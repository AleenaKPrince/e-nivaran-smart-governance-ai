def _pick_variant(options, message):
    if not isinstance(options, list) or not options:
        return options

    key = (message or "").strip()
    if not key:
        return options[0]

    index = sum(ord(ch) for ch in key) % len(options)
    return options[index]


def generate_response(intent, language, message=""):
    lang = "ml" if language == "ml" else "en"
    selected_intent = intent if intent in RESPONSES else "fallback"
    variants = RESPONSES[selected_intent][lang]
    return _pick_variant(variants, message)
